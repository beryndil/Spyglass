def translate_value(value, locale_dict):
    """Try to find a translation for the given value."""
    if value in locale_dict:
        return locale_dict[value]
    return value  # fallback to English


def process_file(locale):
    """Read English strings.xml and write translated version."""
    locale_dict = TRANSLATIONS.get(locale, {})

    with open(EN_FILE, 'r', encoding='utf-8') as f:
        content = f.read()

    # Pattern to match string elements
    pattern = re.compile(r'(<string name="[^"]+">)(.*?)(</string>)')

    def replace_match(m):
        open_tag = m.group(1)
        value = m.group(2)
        close_tag = m.group(3)
        translated = translate_value(value, locale_dict)
        return f'{open_tag}{translated}{close_tag}'

    translated_content = pattern.sub(replace_match, content)

    out_dir = os.path.join(BASE_DIR, f'values-{locale}')
    os.makedirs(out_dir, exist_ok=True)
    out_file = os.path.join(out_dir, 'strings.xml')

    with open(out_file, 'w', encoding='utf-8') as f:
        f.write(translated_content)

    # Count translations
    matches = pattern.findall(content)
    translated_count = sum(1 for _, v, _ in matches if v in locale_dict)
    print(f'{locale}: {translated_count}/{len(matches)} strings translated, {len(matches)-translated_count} kept in English')


if __name__ == '__main__':
    for locale in ['es', 'pt', 'fr', 'de', 'ja']:
        process_file(locale)
    print('Done!')
